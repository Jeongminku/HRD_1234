package DTO;

public class Status {
	String hcode;
	String hname;
	String hcount;
	String fullcount;
	
	public String getFullcount() {
		return fullcount;
	}
	public void setFullcount(String fullcount) {
		this.fullcount = fullcount;
	}
	public String getHcode() {
		return hcode;
	}
	public void setHcode(String hcode) {
		this.hcode = hcode;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getHcount() {
		return hcount;
	}
	public void setHcount(String hcount) {
		this.hcount = hcount;
	}
	
	
}
